def validate_ohlcv(bars: pd.DataFrame) -> pd.DataFrame:
    if not isinstance(bars, pd.DataFrame):
        raise TypeError("bars must be a pandas DataFrame")

    missing = [c for c in REQUIRED_COLUMNS if c not in bars.columns]
    if missing:
        raise ValueError(f"bars is missing required columns: {missing}")

    df = bars.copy()

    if not isinstance(df.index, pd.DatetimeIndex):
        raise TypeError("bars index must be a pandas DatetimeIndex")

    df = df.sort_index()

    if df.index.has_duplicates:
        raise ValueError("bars index contains duplicate timestamps")

    if df[list(REQUIRED_COLUMNS)].isna().any().any():
        raise ValueError("bars contains NaNs in required OHLCV columns")

    if (df["high"] < df[["open", "close", "low"]].max(axis=1)).any():
        raise ValueError("found rows where high is below open/close/low")
    if (df["low"] > df[["open", "close", "high"]].min(axis=1)).any():
        raise ValueError("found rows where low is above open/close/high")
    if (df["volume"] < 0).any():
        raise ValueError("volume must be non-negative")

    return df


def compute_metrics(
    equity_curve: pd.DataFrame,
    trades: pd.DataFrame,
    fills: pd.DataFrame,
    bars_per_year: int,
) -> Dict[str, float]:
    out: Dict[str, float] = {}
    if equity_curve.empty:
        return out

    eq = equity_curve["equity"].astype(float)
    returns = eq.pct_change().replace([np.inf, -np.inf], np.nan).dropna()

    out["total_return"] = float(eq.iloc[-1] / eq.iloc[0] - 1.0)

    n_periods = len(returns)
    if n_periods > 0 and eq.iloc[0] > 0 and eq.iloc[-1] > 0:
        years = n_periods / bars_per_year
        out["annualized_return"] = float((eq.iloc[-1] / eq.iloc[0]) ** (1 / years) - 1.0) if years > 0 else float("nan")
    else:
        out["annualized_return"] = float("nan")

    if n_periods > 1:
        ret_std = returns.std(ddof=0)
        ann_vol = ret_std * math.sqrt(bars_per_year)
        out["annualized_volatility"] = float(ann_vol)
        out["sharpe_ratio"] = float(returns.mean() / ret_std * math.sqrt(bars_per_year)) if ret_std > 0 else float("nan")

        downside = returns[returns < 0]
        downside_std = downside.std(ddof=0) if len(downside) > 0 else 0.0
        out["sortino_ratio"] = float(returns.mean() / downside_std * math.sqrt(bars_per_year)) if downside_std > 0 else float("nan")
    else:
        out["annualized_volatility"] = float("nan")
        out["sharpe_ratio"] = float("nan")
        out["sortino_ratio"] = float("nan")

    running_max = eq.cummax()
    drawdown = eq / running_max - 1.0
    out["max_drawdown"] = float(drawdown.min())

    if out["max_drawdown"] < 0 and np.isfinite(out["annualized_return"]):
        out["calmar_ratio"] = float(out["annualized_return"] / abs(out["max_drawdown"]))
    else:
        out["calmar_ratio"] = float("nan")

    out["exposure"] = float((equity_curve["position"] != 0).mean())

    if not fills.empty and eq.mean() > 0:
        out["turnover"] = float(fills["notional"].sum() / eq.mean())
    else:
        out["turnover"] = 0.0

    out["num_fills"] = int(len(fills))
    out["num_trades"] = int(len(trades))

    if not trades.empty:
        wins = trades["net_pnl"] > 0
        losses = trades["net_pnl"] < 0

        out["win_rate"] = float(wins.mean())
        out["avg_trade_pnl"] = float(trades["net_pnl"].mean())
        out["median_trade_pnl"] = float(trades["net_pnl"].median())
        out["avg_holding_bars"] = float(trades["holding_bars"].mean())

        gross_profit = trades.loc[wins, "net_pnl"].sum()
        gross_loss = -trades.loc[losses, "net_pnl"].sum()
        out["profit_factor"] = float(gross_profit / gross_loss) if gross_loss > 0 else float("inf")

        avg_win = trades.loc[wins, "net_pnl"].mean() if wins.any() else 0.0
        avg_loss = trades.loc[losses, "net_pnl"].mean() if losses.any() else 0.0
        out["avg_win"] = float(avg_win)
        out["avg_loss"] = float(avg_loss)
        out["expectancy"] = float(out["win_rate"] * avg_win + (1.0 - out["win_rate"]) * avg_loss)
    else:
        out["win_rate"] = float("nan")
        out["avg_trade_pnl"] = float("nan")
        out["median_trade_pnl"] = float("nan")
        out["avg_holding_bars"] = float("nan")
        out["profit_factor"] = float("nan")
        out["avg_win"] = float("nan")
        out["avg_loss"] = float("nan")
        out["expectancy"] = float("nan")

    return out

class BarBacktester:
    """
    Single-asset, bar-based backtester.

    Assumptions:
      - the strategy sees bar t after close[t]
      - orders generated on bar t are filled at open[t+1]
      - market orders only
      - one pending order at a time
    """

    def __init__(self, bars: pd.DataFrame, strategy: Strategy, config: Optional[BacktestConfig] = None) -> None:
        self.bars = validate_ohlcv(bars)
        self.strategy = strategy
        self.config = config or BacktestConfig()

    def _fill_details(self, open_price: float, quantity: int | float) -> tuple[float, float, float]:
        slip_per_share = float(open_price) * (self.config.slippage_bps / 10_000.0)
        fill_price = float(open_price + slip_per_share) if quantity > 0 else float(open_price - slip_per_share)
        slip_total = abs(quantity) * slip_per_share
        return fill_price, slip_per_share, slip_total

    def run(self) -> BacktestResult:
        df = self.strategy.prepare(self.bars)
        df = validate_ohlcv(df)

        portfolio = Portfolio(initial_cash=self.config.initial_cash)

        pending_order: Optional[Order] = None
        signals: List[Dict[str, Any]] = []
        fills: List[Dict[str, Any]] = []
        equity_rows: List[Dict[str, Any]] = []
        trades: List[Dict[str, Any]] = []

        current_trade: Optional[Dict[str, Any]] = None
        bars_held = 0

        for i, (ts, row) in enumerate(df.iterrows()):
            prev_position = portfolio.position

            # 1) Fill any order generated at the prior close.
            if pending_order is not None:
                fill_qty = pending_order.quantity
                fill_price, slip_ps, slip_total = self._fill_details(row["open"], fill_qty)

                fill = Fill(
                    timestamp=ts,
                    side="BUY" if fill_qty > 0 else "SELL",
                    quantity=abs(fill_qty),
                    price=fill_price,
                    notional=abs(fill_qty) * fill_price,
                    commission=self.config.commission_per_order,
                    slippage_per_share=slip_ps,
                    slippage_total=slip_total,
                    reason=pending_order.reason,
                )
                realized_from_fill = portfolio.apply_fill(fill)

                fills.append(
                    {
                        "timestamp": fill.timestamp,
                        "side": fill.side,
                        "quantity": fill.quantity,
                        "price": fill.price,
                        "notional": fill.notional,
                        "commission": fill.commission,
                        "slippage_per_share": fill.slippage_per_share,
                        "slippage_total": fill.slippage_total,
                        "reason": fill.reason,
                        "realized_pnl_from_fill": realized_from_fill,
                        "position_after_fill": portfolio.position,
                        "cash_after_fill": portfolio.cash,
                    }
                )

                new_position = portfolio.position
                fee = fill.commission

                # Trade bookkeeping. This is exact for flat->position->flat
                # and also handles reversals by splitting the fee proportionally.
                if prev_position == 0 and new_position != 0:
                    current_trade = {
                        "entry_time": ts,
                        "direction": "LONG" if new_position > 0 else "SHORT",
                        "quantity": abs(new_position),
                        "entry_price": fill.price,
                        "entry_reason": fill.reason,
                        "fees": fee,
                    }
                    bars_held = 0

                elif prev_position != 0 and new_position == 0:
                    if current_trade is not None:
                        qty = current_trade["quantity"]
                        exit_price = fill.price
                        gross = (exit_price - current_trade["entry_price"]) * qty if current_trade["direction"] == "LONG" \
                            else (current_trade["entry_price"] - exit_price) * qty
                        total_fees = current_trade["fees"] + fee
                        trades.append(
                            {
                                "entry_time": current_trade["entry_time"],
                                "exit_time": ts,
                                "direction": current_trade["direction"],
                                "quantity": qty,
                                "entry_price": current_trade["entry_price"],
                                "exit_price": exit_price,
                                "gross_pnl": gross,
                                "fees": total_fees,
                                "net_pnl": gross - total_fees,
                                "holding_bars": bars_held,
                                "entry_reason": current_trade["entry_reason"],
                                "exit_reason": fill.reason,
                            }
                        )
                    current_trade = None
                    bars_held = 0

                elif prev_position != 0 and np.sign(prev_position) != np.sign(new_position):
                    close_qty = abs(prev_position)
                    open_qty = abs(new_position)
                    total_qty = close_qty + open_qty
                    close_fee = fee * close_qty / total_qty
                    open_fee = fee * open_qty / total_qty

                    if current_trade is not None:
                        qty = current_trade["quantity"]
                        exit_price = fill.price
                        gross = (exit_price - current_trade["entry_price"]) * qty if current_trade["direction"] == "LONG" \
                            else (current_trade["entry_price"] - exit_price) * qty
                        total_fees = current_trade["fees"] + close_fee
                        trades.append(
                            {
                                "entry_time": current_trade["entry_time"],
                                "exit_time": ts,
                                "direction": current_trade["direction"],
                                "quantity": qty,
                                "entry_price": current_trade["entry_price"],
                                "exit_price": exit_price,
                                "gross_pnl": gross,
                                "fees": total_fees,
                                "net_pnl": gross - total_fees,
                                "holding_bars": bars_held,
                                "entry_reason": current_trade["entry_reason"],
                                "exit_reason": fill.reason,
                            }
                        )

                    current_trade = {
                        "entry_time": ts,
                        "direction": "LONG" if new_position > 0 else "SHORT",
                        "quantity": abs(new_position),
                        "entry_price": fill.price,
                        "entry_reason": fill.reason,
                        "fees": open_fee,
                    }
                    bars_held = 0

                else:
                    # Same-side scaling or partial reduction.
                    if current_trade is not None:
                        current_trade["quantity"] = abs(portfolio.position)
                        current_trade["entry_price"] = portfolio.avg_price
                        current_trade["fees"] += fee

                pending_order = None

            # 2) Update bars held after the fill.
            if portfolio.position != 0:
                bars_held += 1
            else:
                bars_held = 0

            # 3) Mark to market on the close.
            eq = portfolio.equity(row["close"])
            equity_rows.append(
                {
                    "timestamp": ts,
                    "open": row["open"],
                    "high": row["high"],
                    "low": row["low"],
                    "close": row["close"],
                    "volume": row["volume"],
                    "position": portfolio.position,
                    "avg_price": portfolio.avg_price,
                    "cash": portfolio.cash,
                    "realized_pnl": portfolio.realized_pnl,
                    "unrealized_pnl": portfolio.unrealized_pnl(row["close"]),
                    "equity": eq,
                }
            )

            # 4) Generate target position for next bar.
            if i < len(df) - 1:
                state = StrategyState(
                    current_position=portfolio.position,
                    bars_held=bars_held,
                    cash=portfolio.cash,
                    equity=eq,
                    avg_price=portfolio.avg_price,
                )
                target = self.strategy.target_position(row, state)

                if not self.config.allow_fractional and not float(target).is_integer():
                    raise ValueError("strategy returned fractional target_position while allow_fractional=False")

                delta = target - portfolio.position
                delta = int(delta) if float(delta).is_integer() else float(delta)

                signals.append(
                    {
                        "timestamp": ts,
                        "close": row["close"],
                        "target_position": target,
                        "current_position": portfolio.position,
                        "order_quantity_next_bar": delta,
                        "zscore": row.get("zscore", np.nan),
                    }
                )

                if delta != 0:
                    if target == 0:
                        reason = "exit"
                    elif portfolio.position == 0 and target > 0:
                        reason = "enter_long"
                    elif portfolio.position == 0 and target < 0:
                        reason = "enter_short"
                    elif np.sign(target) != np.sign(portfolio.position):
                        reason = "reverse"
                    else:
                        reason = "rebalance"
                    pending_order = Order(submitted_at=ts, quantity=delta, reason=reason)

        equity_df = pd.DataFrame(equity_rows).set_index("timestamp")

        if signals:
            signals_df = pd.DataFrame(signals).set_index("timestamp")
        else:
            signals_df = pd.DataFrame(
                columns=["close", "target_position", "current_position", "order_quantity_next_bar", "zscore"]
            )

        fills_df = pd.DataFrame(fills)
        if not fills_df.empty:
            fills_df = fills_df.set_index("timestamp")
        else:
            fills_df = pd.DataFrame(
                columns=[
                    "side",
                    "quantity",
                    "price",
                    "notional",
                    "commission",
                    "slippage_per_share",
                    "slippage_total",
                    "reason",
                    "realized_pnl_from_fill",
                    "position_after_fill",
                    "cash_after_fill",
                ]
            )

        trades_df = pd.DataFrame(trades)
        if not trades_df.empty:
            trades_df["entry_time"] = pd.to_datetime(trades_df["entry_time"])
            trades_df["exit_time"] = pd.to_datetime(trades_df["exit_time"])

        metrics = compute_metrics(
            equity_curve=equity_df,
            trades=trades_df,
            fills=fills_df.reset_index() if not fills_df.empty else fills_df,
            bars_per_year=self.config.bars_per_year,
        )

        return BacktestResult(
            bars=df,
            signals=signals_df,
            equity_curve=equity_df,
            fills=fills_df,
            trades=trades_df,
            metrics=metrics,
        )


