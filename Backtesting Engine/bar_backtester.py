from __future__ import annotations

from dataclasses import dataclass
from typing import Optional, Dict, Any, List
import math

import numpy as np
import pandas as pd


REQUIRED_COLUMNS = ("open", "high", "low", "close", "volume")

@dataclass
class BacktestConfig:
    initial_cash: float = 100_000.0
    commission_per_order: float = 1.00
    slippage_bps: float = 2.0
    bars_per_year: int = 252
    allow_fractional: bool = False


@dataclass
class Order:
    submitted_at: pd.Timestamp
    quantity: int | float
    reason: str = ""


@dataclass
class Fill:
    timestamp: pd.Timestamp
    side: str
    quantity: int | float
    price: float
    notional: float
    commission: float
    slippage_per_share: float
    slippage_total: float
    reason: str = ""


@dataclass
class StrategyState:
    current_position: int | float
    bars_held: int
    cash: float
    equity: float
    avg_price: float


@dataclass
class BacktestResult:
    bars: pd.DataFrame
    signals: pd.DataFrame
    equity_curve: pd.DataFrame
    fills: pd.DataFrame
    trades: pd.DataFrame
    metrics: Dict[str, float]

class Strategy:
    def prepare(self, bars: pd.DataFrame) -> pd.DataFrame:
        return bars.copy()

    def target_position(self, row: pd.Series, state: StrategyState) -> int | float:
        raise NotImplementedError("Strategy subclasses must implement target_position().")


class MeanReversionStrategy(Strategy):
    """
    Z-score mean reversion.

    Rules:
      - z = (close - rolling_mean) / rolling_std
      - long when z <= -entry_z
      - short when z >= +entry_z
      - exit to flat when abs(z) <= exit_z
      - optional max_hold_bars to force an exit
    """

    def __init__(
        self,
        lookback: int = 20,
        entry_z: float = 1.5,
        exit_z: float = 0.5,
        trade_size: int = 100,
        allow_short: bool = True,
        max_hold_bars: Optional[int] = None,
        min_std: float = 1e-12,
    ) -> None:
        if lookback < 2:
            raise ValueError("lookback must be >= 2")
        if exit_z >= entry_z:
            raise ValueError("exit_z should be smaller than entry_z")
        if trade_size <= 0:
            raise ValueError("trade_size must be > 0")
        self.lookback = lookback
        self.entry_z = entry_z
        self.exit_z = exit_z
        self.trade_size = int(trade_size)
        self.allow_short = allow_short
        self.max_hold_bars = max_hold_bars
        self.min_std = min_std

    def prepare(self, bars: pd.DataFrame) -> pd.DataFrame:
        df = bars.copy()
        df["mean"] = df["close"].rolling(self.lookback, min_periods=self.lookback).mean()
        df["std"] = df["close"].rolling(self.lookback, min_periods=self.lookback).std(ddof=0)
        df["std"] = df["std"].where(df["std"] > self.min_std, np.nan)
        df["zscore"] = (df["close"] - df["mean"]) / df["std"]
        return df

    def target_position(self, row: pd.Series, state: StrategyState) -> int:
        z = row.get("zscore", np.nan)
        if pd.isna(z):
            return int(state.current_position)

        # Fail-safe time stop.
        if self.max_hold_bars is not None and state.current_position != 0 and state.bars_held >= self.max_hold_bars:
            return 0

        pos = int(state.current_position)

        if pos == 0:
            if z <= -self.entry_z:
                return self.trade_size
            if self.allow_short and z >= self.entry_z:
                return -self.trade_size
            return 0

        if pos > 0:
            if abs(z) <= self.exit_z or z >= self.entry_z:
                return 0
            return pos

        # pos < 0
        if abs(z) <= self.exit_z or z <= -self.entry_z:
            return 0
        return pos

class Portfolio:
    def __init__(self, initial_cash: float) -> None:
        self.cash = float(initial_cash)
        self.position = 0
        self.avg_price = 0.0
        self.realized_pnl = 0.0

    def equity(self, mark_price: float) -> float:
        return self.cash + self.position * float(mark_price)

    def unrealized_pnl(self, mark_price: float) -> float:
        if self.position == 0:
            return 0.0
        if self.position > 0:
            return (float(mark_price) - self.avg_price) * self.position
        return (self.avg_price - float(mark_price)) * abs(self.position)

    def apply_fill(self, fill: Fill) -> float:
        """
        Applies a signed fill and returns the realized pnl generated by this fill,
        excluding commissions (commissions are deducted directly from cash).
        """
        signed_qty = fill.quantity if fill.side == "BUY" else -fill.quantity
        signed_qty = int(signed_qty) if float(signed_qty).is_integer() else float(signed_qty)

        # Buying spends cash; selling receives cash.
        self.cash -= fill.price * signed_qty
        self.cash -= fill.commission

        if self.position == 0:
            self.position = signed_qty
            self.avg_price = fill.price
            return 0.0

        # Add to same-side position -> update weighted average.
        if np.sign(self.position) == np.sign(signed_qty):
            new_position = self.position + signed_qty
            self.avg_price = (
                abs(self.position) * self.avg_price + abs(signed_qty) * fill.price
            ) / abs(new_position)
            self.position = new_position
            return 0.0

        # Opposite-side fill -> partial close, full close, or reversal.
        closing_qty = min(abs(self.position), abs(signed_qty))
        if self.position > 0:
            realized = (fill.price - self.avg_price) * closing_qty
        else:
            realized = (self.avg_price - fill.price) * closing_qty

        self.realized_pnl += realized
        new_position = self.position + signed_qty

        if new_position == 0:
            self.position = 0
            self.avg_price = 0.0
            return realized

        if np.sign(new_position) == np.sign(self.position):
            # Partial reduction. Avg price stays the same.
            self.position = new_position
            return realized

        # Reversal. Leftover quantity becomes a new position at the fill price.
        self.position = new_position
        self.avg_price = fill.price
        return realized


