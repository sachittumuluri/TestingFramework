def make_oscillating_ohlcv(n: int = 500, freq: str = "D", seed: int = 7) -> pd.DataFrame:
    """
    Oscillating price path that mean reversion should usually handle better.
    """
    rng = np.random.default_rng(seed)
    idx = pd.date_range("2020-01-01", periods=n, freq=freq)
    base = 100.0 + 5.0 * np.sin(np.linspace(0, 40 * np.pi, n))  # ~20 cycles
    close = base + rng.normal(0.0, 0.3, size=n)
    open_ = np.roll(close, 1)
    open_[0] = close[0]
    high = np.maximum(open_, close) + rng.uniform(0.1, 0.7, size=n)
    low = np.minimum(open_, close) - rng.uniform(0.1, 0.7, size=n)
    volume = rng.integers(5_000, 15_000, size=n)
    return pd.DataFrame({"open": open_, "high": high, "low": low, "close": close, "volume": volume}, index=idx)


def make_trending_ohlcv(n: int = 500, freq: str = "D", seed: int = 7) -> pd.DataFrame:
    """
    Strong trend path that mean reversion should usually struggle against.
    """
    rng = np.random.default_rng(seed)
    idx = pd.date_range("2020-01-01", periods=n, freq=freq)
    close = 100.0 + np.linspace(0.0, 80.0, n) + rng.normal(0.0, 0.1, size=n)
    open_ = np.roll(close, 1)
    open_[0] = close[0]
    high = np.maximum(open_, close) + rng.uniform(0.1, 0.7, size=n)
    low = np.minimum(open_, close) - rng.uniform(0.1, 0.7, size=n)
    volume = rng.integers(5_000, 15_000, size=n)
    return pd.DataFrame({"open": open_, "high": high, "low": low, "close": close, "volume": volume}, index=idx)


def make_random_walk_ohlcv(n: int = 500, freq: str = "D", seed: int = 7) -> pd.DataFrame:
    rng = np.random.default_rng(seed)
    idx = pd.date_range("2020-01-01", periods=n, freq=freq)
    returns = rng.normal(0.0, 0.01, size=n)
    close = 100.0 * np.cumprod(1.0 + returns)
    open_ = np.roll(close, 1)
    open_[0] = close[0]
    high = np.maximum(open_, close) + rng.uniform(0.1, 0.7, size=n)
    low = np.minimum(open_, close) - rng.uniform(0.1, 0.7, size=n)
    volume = rng.integers(5_000, 15_000, size=n)
    return pd.DataFrame({"open": open_, "high": high, "low": low, "close": close, "volume": volume}, index=idx)


def run_validation_suite() -> Dict[str, Dict[str, float]]:
    datasets = {
        "oscillating": make_oscillating_ohlcv(),
        "trending": make_trending_ohlcv(),
        "random_walk": make_random_walk_ohlcv(),
    }

    strategy = MeanReversionStrategy(
        lookback=20,
        entry_z=1.5,
        exit_z=0.5,
        trade_size=100,
        allow_short=True,
        max_hold_bars=15,
    )

    config = BacktestConfig(
        initial_cash=100_000.0,
        commission_per_order=1.0,
        slippage_bps=2.0,
        bars_per_year=252,
    )

    results: Dict[str, Dict[str, float]] = {}
    for name, bars in datasets.items():
        bt = BarBacktester(bars=bars, strategy=strategy, config=config)
        results[name] = bt.run().metrics
    return results


